        public Backend()
        {
            board = new int[70, 200];
            for (int i = 0; i < board.GetLength(0); i++)
            {
                for (int j = 0; j < board.GetLength(1); j++)
                {
                    board[i, j] = 0;
                }
            }

            generations = 1;
            stabilizationOccurred = true;
        }

        public Backend(int x, int y)
        {
            board = new int[x, y];
        }

        public void placeObject(int[,] structure, int y, int x)
        {
            int startX = x;
            int startY = y;
            for (int i = 0; i < structure.GetLength(0); i++)
            {
                for (int j = 0; j < structure.GetLength(1); j++)
                {
                    board[y, x++] = structure[i, j];
                }
                x = startX;
                y++;
            }
        }

        public int[,] getBoard()
        {
            return board;
        }

        public void printBoard(Object source, System.Timers.ElapsedEventArgs e)
        {
            for (int i = 0; i < board.GetLength(0); i++)
            {
                for (int j = 0; j < board.GetLength(1); j++)
                {
                    Console.SetCursorPosition(j, i); // this makes printing smooth!
                    if (board[i, j] == 0)
                        Console.Write(" ");
                    else
                        Console.Write("+");
                }
                Console.WriteLine();
            }
            Console.WriteLine("Generations: " + generations + "\nPress Enter to exit");
            updateBoard();
            generations++;
        }

        public void updateBoard()
        {
            #region tempBoard definition
            int[,] tempBoard = new int[70, 200];
            for (int i = 0; i < board.GetLength(0); i++)
            {
                for (int j = 0; j < board.GetLength(1); j++)
                {
                    tempBoard[i, j] = 0;
                }
            }
            #endregion
            for (int i = 0; i < board.GetLength(0); i++) // y axis
            {
                for (int j = 0; j < board.GetLength(1); j++) // x axis
                {
                    int neighbors = checkLive(i, j);
                    if (board[i, j] == 1) // removing this check produces odd behavior...
                    {
                        if (neighbors == 2 || neighbors == 3)
                        {
                            tempBoard[i, j] = 1;
                        }
                        else
                        {
                            tempBoard[i, j] = 0;
                        }
                    }
                    if (board[i, j] == 0 && neighbors == 3) // come to life!
                    {
                        tempBoard[i, j] = 1;
                    }
                }
            }
            // checks for stabilization
            Boolean stable = true;
            for (int i = 0; i < board.GetLength(0); i++)
            {
                for (int j = 0; j < board.GetLength(1); j++)
                {
                    if (!(board[i, j] == tempBoard[i, j]))
                    {
                        stable = false;
                        stabilizationOccurred = false;
                        break;
                    }
                }
            }
            if (stable && stabilizationOccurred)
            {
                //Console.WriteLine("Stabilization occured at generation: " + generations);
                stabilizationOccurred = false; // prevents another printing of our stable-generation-number thing
            }
            board = tempBoard;
        }

