        void setStructureLabelText()
        {
            String boardString = "";

            for (int i = 0; i < structure.GetLength(0); i++)
            {
                for (int j = 0; j < structure.GetLength(1); j++)
                {
                    if (structure[i, j] == 0)
                        boardString += ". ";
                    else
                    {
                        boardString += "+";
                    }
                }
                boardString += "\n";
            }
            selectedStructureLabel.Text = boardString;
        }

